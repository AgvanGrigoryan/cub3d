#include "cub3d.h"

int	is_player(char s, int *pl)
{
	if (s == 'N' || s == 'n'
		|| s == 'S' || s == 's'
		|| s == 'E' || s == 'e'
		|| s == 'W' || s == 'w')
	{
		if (*pl == 0)
			*pl = 1;
		else
			return (-1);
		return (0);
	}
	else
		return (0);
}
